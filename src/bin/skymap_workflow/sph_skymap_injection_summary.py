#!/usr/bin/env python3

import argparse
import csv
import os
import sys

os.environ.setdefault("MPLCONFIGDIR", os.path.join("/tmp", "matplotlib"))

import healpy
import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy
from ligo.skymap.io import read_sky_map


DEFAULT_COLUMNS = (
	"simulation_id",
	"coinc_event_id",
	"instruments",
	"n_ifo",
	"snr",
	"combined_far",
	"false_alarm_rate",
	"likelihood",
	"ra_deg",
	"dec_deg",
	"nside",
	"credible_level",
	"in_50",
	"in_90",
	"area_50_deg2",
	"area_90_deg2",
	"searched_area_deg2",
	"peak_ra_deg",
	"peak_dec_deg",
	"bayestar_nside",
	"bayestar_credible_level",
	"bayestar_in_50",
	"bayestar_in_90",
	"bayestar_area_50_deg2",
	"bayestar_area_90_deg2",
	"bayestar_searched_area_deg2",
	"bayestar_peak_ra_deg",
	"bayestar_peak_dec_deg",
	"searched_area_ratio_to_bayestar",
)


def log(message):
	print(f"[sph_skymap_injection_summary] {message}", file = sys.stderr)


def parse_args():
	parser = argparse.ArgumentParser(
		description = "Summarize sphradiometer injection skymaps with paper-style localization diagnostics."
	)
	parser.add_argument("--manifest", default = "outputs/autogenerated_manifest.csv", help = "manifest CSV containing simulation_id, ra_deg, and dec_deg")
	parser.add_argument("--skymap-dir", default = "outputs/skymap", help = "directory containing sphradiometer FITS maps")
	parser.add_argument("--skymap-pattern", default = "sim{simulation_id}.fits", help = "format string for sphradiometer maps")
	parser.add_argument("--bayestar-dir", default = None, help = "optional directory containing matching BAYESTAR FITS maps")
	parser.add_argument("--bayestar-pattern", default = "sim{simulation_id}_bayestar.fits", help = "format string for BAYESTAR maps")
	parser.add_argument("--output-dir", default = "outputs/summary", help = "directory for summary CSV and figures")
	parser.add_argument("--summary-csv", default = "summary.csv", help = "summary CSV filename inside output-dir")
	parser.add_argument("--containment-summary", default = "containment_summary.txt", help = "aggregate containment summary filename inside output-dir")
	parser.add_argument("--fig3", default = "fig3_pp_plot.png", help = "Fig. 3-style P-P plot filename")
	parser.add_argument("--fig4", default = "fig4_searched_area.png", help = "Fig. 4-style searched-area distribution filename")
	parser.add_argument("--fig5", default = "fig5_area_ratio_to_bayestar.png", help = "Fig. 5-style area-ratio distribution filename")
	parser.add_argument("--fig90", default = "fig90_searched_over_area90.png", help = "searched-area / 90%%-area containment-margin plot filename")
	parser.add_argument("--combo-plot-dir", default = "by_combo", help = "subdirectory inside output-dir for detector-combo figures")
	parser.add_argument("--no-combo-plots", action = "store_true", help = "do not write per-detector-combo figure sets")
	parser.add_argument("--missing-ok", action = "store_true", help = "skip manifest rows whose sphradiometer FITS map is missing")
	parser.add_argument("--max-rows", type = int, default = None, help = "optional maximum number of manifest rows to inspect")
	parser.add_argument("--only-instruments", default = "", help = "comma-separated detector combos to include, e.g. H1L1,H1L1V1")
	return parser.parse_args()


def read_manifest(path, max_rows = None, only_instruments = None):
	rows = []
	with open(path, "r", newline = "") as f:
		reader = csv.DictReader(f)
		for row in reader:
			if only_instruments and row.get("instruments", "") not in only_instruments:
				continue
			rows.append(row)
			if max_rows is not None and len(rows) >= max_rows:
				break
	return rows


def format_path(directory, pattern, row):
	return os.path.join(directory, pattern.format(**row))


def read_probability(path):
	prob, _metadata = read_sky_map(path)
	prob = numpy.asarray(prob, dtype = float)
	if prob.ndim != 1:
		raise ValueError(f"{path}: expected a 1-D HEALPix probability vector, got shape {prob.shape}")
	total = prob.sum()
	if not numpy.isfinite(total) or total <= 0.0:
		raise ValueError(f"{path}: probability map has non-positive or non-finite total probability")
	return prob / total


def map_metrics(path, ra_deg, dec_deg):
	prob = read_probability(path)
	nside = healpy.npix2nside(len(prob))
	theta = numpy.radians(90.0 - dec_deg)
	phi = numpy.radians(ra_deg)
	true_pix = int(healpy.ang2pix(nside, theta, phi))
	order = numpy.argsort(prob)[::-1]
	cumulative = numpy.cumsum(prob[order])
	rank = numpy.empty_like(order)
	rank[order] = numpy.arange(order.size)
	true_rank = int(rank[true_pix])
	pixel_area = healpy.nside2pixarea(nside, degrees = True)
	peak_pix = int(order[0])
	peak_theta, peak_phi = healpy.pix2ang(nside, peak_pix)
	idx50 = int(numpy.searchsorted(cumulative, 0.5, side = "left"))
	idx90 = int(numpy.searchsorted(cumulative, 0.9, side = "left"))
	credible = float(cumulative[true_rank])
	return {
		"nside": nside,
		"credible_level": credible,
		"in_50": int(credible <= 0.5),
		"in_90": int(credible <= 0.9),
		"area_50_deg2": float((idx50 + 1) * pixel_area),
		"area_90_deg2": float((idx90 + 1) * pixel_area),
		"searched_area_deg2": float((true_rank + 1) * pixel_area),
		"peak_ra_deg": float(numpy.degrees(peak_phi) % 360.0),
		"peak_dec_deg": float(90.0 - numpy.degrees(peak_theta)),
	}


def event_id(row):
	return row.get("simulation_id") or row.get("event_id") or row.get("coinc_event_id")


def summarize(args):
	only_instruments = {x.strip() for x in args.only_instruments.split(",") if x.strip()}
	rows = read_manifest(args.manifest, max_rows = args.max_rows, only_instruments = only_instruments)
	log(f"read {len(rows)} manifest rows")
	os.makedirs(args.output_dir, exist_ok = True)
	summary_rows = []
	missing = 0
	for row in rows:
		sim = event_id(row)
		if sim is None:
			raise ValueError("manifest row has no simulation_id, event_id, or coinc_event_id")
		row = dict(row)
		row.setdefault("simulation_id", sim)
		try:
			ra_deg = float(row["ra_deg"])
			dec_deg = float(row["dec_deg"])
		except KeyError as exc:
			raise KeyError("manifest must contain ra_deg and dec_deg for injection summary") from exc
		path = format_path(args.skymap_dir, args.skymap_pattern, row)
		if not os.path.exists(path):
			missing += 1
			if args.missing_ok:
				continue
			raise FileNotFoundError(path)
		metrics = map_metrics(path, ra_deg, dec_deg)
		out = {key: row.get(key, "") for key in DEFAULT_COLUMNS}
		out.update({
			"simulation_id": row.get("simulation_id", ""),
			"coinc_event_id": row.get("coinc_event_id", ""),
			"instruments": row.get("instruments", ""),
			"n_ifo": len(row.get("instruments", "")) // 2,
			"snr": row.get("snr", ""),
			"combined_far": row.get("combined_far", ""),
			"false_alarm_rate": row.get("false_alarm_rate", ""),
			"likelihood": row.get("likelihood", ""),
			"ra_deg": row.get("ra_deg", ""),
			"dec_deg": row.get("dec_deg", ""),
		})
		out.update(metrics)
		if args.bayestar_dir:
			bpath = format_path(args.bayestar_dir, args.bayestar_pattern, row)
			if os.path.exists(bpath):
				bmetrics = map_metrics(bpath, ra_deg, dec_deg)
				for key, value in bmetrics.items():
					out[f"bayestar_{key}"] = value
				if bmetrics["searched_area_deg2"] > 0.0:
					out["searched_area_ratio_to_bayestar"] = metrics["searched_area_deg2"] / bmetrics["searched_area_deg2"]
		summary_rows.append(out)
	if missing:
		log(f"skipped {missing} rows with missing sphradiometer maps")
	return summary_rows


def write_summary(path, rows):
	with open(path, "w", newline = "") as f:
		writer = csv.DictWriter(f, fieldnames = DEFAULT_COLUMNS, extrasaction = "ignore")
		writer.writeheader()
		writer.writerows(rows)
	log(f"wrote {path}")


def aggregate_containment(rows, prefix = ""):
	credible = numeric_values(rows, f"{prefix}credible_level")
	area50 = numeric_values(rows, f"{prefix}area_50_deg2")
	area90 = numeric_values(rows, f"{prefix}area_90_deg2")
	searched = numeric_values(rows, f"{prefix}searched_area_deg2")
	if len(credible) == 0:
		return None
	n = len(credible)
	in50 = int((credible <= 0.5).sum())
	in90 = int((credible <= 0.9).sum())
	return {
		"N": n,
		"in50": in50,
		"in50_fraction": in50 / n,
		"in90": in90,
		"in90_fraction": in90 / n,
		"mean_area50": float(area50.mean()) if len(area50) else float("nan"),
		"mean_area90": float(area90.mean()) if len(area90) else float("nan"),
		"mean_searched": float(searched.mean()) if len(searched) else float("nan"),
	}


def format_containment(label, summary):
	return (
		f"{label}:\n"
		f"  N={summary['N']}\n"
		f"  in50={summary['in50']} ({summary['in50_fraction']:.3f})\n"
		f"  in90={summary['in90']} ({summary['in90_fraction']:.3f})\n"
		f"  mean_area50={summary['mean_area50']:.0f} deg2\n"
		f"  mean_area90={summary['mean_area90']:.0f} deg2\n"
		f"  mean_searched={summary['mean_searched']:.0f} deg2"
	)


def write_containment_summary(path, rows):
	blocks = []
	summary = aggregate_containment(rows)
	if summary is not None:
		blocks.append(format_containment("sphradiometer/all", summary))
	for instruments in sorted({row.get("instruments", "") for row in rows if row.get("instruments", "")}):
		combo_rows = [row for row in rows if row.get("instruments", "") == instruments]
		combo_summary = aggregate_containment(combo_rows)
		if combo_summary is not None:
			blocks.append(format_containment(f"sphradiometer/{instruments}", combo_summary))
	bayestar_summary = aggregate_containment(rows, prefix = "bayestar_")
	if bayestar_summary is not None:
		blocks.append(format_containment("BAYESTAR/all", bayestar_summary))
	for instruments in sorted({row.get("instruments", "") for row in rows if row.get("instruments", "")}):
		combo_rows = [row for row in rows if row.get("instruments", "") == instruments]
		combo_summary = aggregate_containment(combo_rows, prefix = "bayestar_")
		if combo_summary is not None:
			blocks.append(format_containment(f"BAYESTAR/{instruments}", combo_summary))
	text = "\n\n".join(blocks)
	if text:
		print(text)
		with open(path, "w") as f:
			f.write(text + "\n")
		log(f"wrote {path}")


def numeric_values(rows, key):
	values = []
	for row in rows:
		value = row.get(key, "")
		if value == "":
			continue
		try:
			value = float(value)
		except (TypeError, ValueError):
			continue
		if numpy.isfinite(value):
			values.append(value)
	return numpy.asarray(values, dtype = float)


def paired_numeric_ratios(rows, numerator_key, denominator_key):
	values = []
	for row in rows:
		numerator = row.get(numerator_key, "")
		denominator = row.get(denominator_key, "")
		if numerator == "" or denominator == "":
			continue
		try:
			numerator = float(numerator)
			denominator = float(denominator)
		except (TypeError, ValueError):
			continue
		if numpy.isfinite(numerator) and numpy.isfinite(denominator) and denominator > 0.0:
			values.append(numerator / denominator)
	return numpy.asarray(values, dtype = float)


def plot_pp(path, rows):
	x = numpy.linspace(0.0, 1.0, 501)
	fig, ax = plt.subplots(figsize = (6.0, 5.5))
	for key, label in (("credible_level", "sphradiometer"), ("bayestar_credible_level", "BAYESTAR")):
		values = numeric_values(rows, key)
		if len(values) == 0:
			continue
		y = numpy.array([(values <= xx).mean() for xx in x])
		ax.plot(x, y, label = f"{label} (N={len(values)})")
	ax.plot([0, 1], [0, 1], "k--", linewidth = 1.0, label = "target")
	ax.set_xlim(0, 1)
	ax.set_ylim(0, 1)
	ax.set_xlabel("searched posterior mass")
	ax.set_ylabel("cumulative fraction of injections")
	ax.grid(True, alpha = 0.3)
	ax.legend(loc = "best")
	fig.tight_layout()
	fig.savefig(path, dpi = 160)
	plt.close(fig)
	log(f"wrote {path}")


def plot_distribution(path, rows):
	fig, ax = plt.subplots(figsize = (6.4, 5.0))
	for key, label in (("searched_area_deg2", "sphradiometer"), ("bayestar_searched_area_deg2", "BAYESTAR")):
		values = numeric_values(rows, key)
		values = values[values > 0.0]
		if len(values) == 0:
			continue
		bins = numpy.logspace(numpy.log10(values.min()), numpy.log10(values.max()), 70) if values.min() < values.max() else 20
		ax.hist(values, bins = bins, histtype = "step", density = True, label = f"{label} (N={len(values)})")
	ax.set_xscale("log")
	ax.set_xlabel("searched area [deg$^2$]")
	ax.set_ylabel("sample density")
	ax.grid(True, which = "both", alpha = 0.3)
	ax.legend(loc = "best")
	fig.tight_layout()
	fig.savefig(path, dpi = 160)
	plt.close(fig)
	log(f"wrote {path}")


def plot_ratio(path, rows):
	values = numeric_values(rows, "searched_area_ratio_to_bayestar")
	values = values[values > 0.0]
	if len(values) == 0:
		log("no BAYESTAR ratios available; not writing Fig. 5-style plot")
		return
	fig, ax = plt.subplots(figsize = (6.4, 5.0))
	bins = numpy.logspace(numpy.log10(values.min()), numpy.log10(values.max()), 70) if values.min() < values.max() else 20
	ax.hist(values, bins = bins, histtype = "step", density = True)
	ax.axvline(1.0, color = "k", linestyle = "--", linewidth = 1.0, label = "equal area")
	ax.axvline(10.0, color = "0.5", linestyle = ":", linewidth = 1.0, label = "10x")
	ax.set_xscale("log")
	ax.set_xlabel("searched area ratio: sphradiometer / BAYESTAR")
	ax.set_ylabel("fraction of injections")
	ax.grid(True, which = "both", alpha = 0.3)
	ax.legend(loc = "best")
	fig.tight_layout()
	fig.savefig(path, dpi = 160)
	plt.close(fig)
	log(f"wrote {path}")


def plot_search_over_area90(path, rows):
	fig, ax = plt.subplots(figsize = (6.4, 5.0))
	wrote = False
	for searched_key, area90_key, label in (
		("searched_area_deg2", "area_90_deg2", "sphradiometer"),
		("bayestar_searched_area_deg2", "bayestar_area_90_deg2", "BAYESTAR"),
	):
		ratio = paired_numeric_ratios(rows, searched_key, area90_key)
		if len(ratio) == 0:
			continue
		ratio.sort()
		y = numpy.arange(1, len(ratio) + 1) / len(ratio)
		ax.plot(ratio, y, label = f"{label} (N={len(ratio)})")
		wrote = True
	if not wrote:
		log("no searched-area / area90 values available; not writing containment-margin plot")
		plt.close(fig)
		return
	ax.axvline(1.0, color = "k", linestyle = "--", linewidth = 1.0, label = "90% contour")
	ax.set_xscale("log")
	ax.set_xlabel("searched area / 90% credible area")
	ax.set_ylabel("cumulative fraction of injections")
	ax.grid(True, which = "both", alpha = 0.3)
	ax.legend(loc = "best")
	fig.tight_layout()
	fig.savefig(path, dpi = 160)
	plt.close(fig)
	log(f"wrote {path}")


def safe_combo_name(instruments):
	return "".join(c if c.isalnum() else "_" for c in instruments)


def plot_combo_figures(output_dir, args, rows):
	combo_dir = os.path.join(output_dir, args.combo_plot_dir)
	os.makedirs(combo_dir, exist_ok = True)
	for instruments in sorted({row.get("instruments", "") for row in rows if row.get("instruments", "")}):
		combo_rows = [row for row in rows if row.get("instruments", "") == instruments]
		name = safe_combo_name(instruments)
		plot_pp(os.path.join(combo_dir, f"{name}_{args.fig3}"), combo_rows)
		plot_distribution(os.path.join(combo_dir, f"{name}_{args.fig4}"), combo_rows)
		plot_ratio(os.path.join(combo_dir, f"{name}_{args.fig5}"), combo_rows)
		plot_search_over_area90(os.path.join(combo_dir, f"{name}_{args.fig90}"), combo_rows)


def main():
	args = parse_args()
	rows = summarize(args)
	if not rows:
		raise RuntimeError("no rows to summarize")
	summary_csv = os.path.join(args.output_dir, args.summary_csv)
	write_summary(summary_csv, rows)
	write_containment_summary(os.path.join(args.output_dir, args.containment_summary), rows)
	plot_pp(os.path.join(args.output_dir, args.fig3), rows)
	plot_distribution(os.path.join(args.output_dir, args.fig4), rows)
	plot_ratio(os.path.join(args.output_dir, args.fig5), rows)
	plot_search_over_area90(os.path.join(args.output_dir, args.fig90), rows)
	if not args.no_combo_plots:
		plot_combo_figures(args.output_dir, args, rows)
	log(f"summarized {len(rows)} skymaps")


if __name__ == "__main__":
	main()
